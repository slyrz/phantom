testRC4 :: String -> ([Word8],[(Int,[Word8])]) -> Test
testRC4 name (key,vectors) =
  let
    (act, exp) = unzip $  testRC4checkVectors (initRC4 key) 0 vectors
  in
    testCase name (act @?= exp)

testRC4checkVectors :: State -> Int -> [(Int,[Word8])] -> [([Word8], [Word8])]
testRC4checkVectors _ _ [] = []
testRC4checkVectors prng offset ((start,exp):vectors) =
  let
    (prng', act) = testRC4SkipAndTake prng offset start (length exp)
  in
    (act, exp) : testRC4checkVectors prng' (start + length exp) vectors

testRC4SkipAndTake :: State -> Int -> Int -> Int -> (State, [Word8])
testRC4SkipAndTake prng offset start size =
  if offset < start
  then
    let
      (prng', _) = takeRC4 prng (start - offset)
    in
      takeRC4 prng' size
  else
    takeRC4 prng size

testHash :: String -> [[Word8]] -> Test
testHash name exp =
  let
    act = take (length exp) $ hash . BS.pack . head $ exp
  in
    testCase name (act @?= exp)

